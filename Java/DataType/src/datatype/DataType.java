
package datatype;

public class DataType {
    
    public static void main(String[] args) {
        
        boolean b = true;
        char c = 'a';
        short s = 31231;
        int i = 12345;
        float f = 10.2f;
        double d = 10.2;
        
        System.out.printf("b = %b\n",b);  //System.out.println("b = "+b);
        System.out.println("c = "+c);
        System.out.println("s = "+s);
        System.out.println("i = "+i);
        System.out.printf("f = %.2f\n",f);   //System.out.println("f = "+f);
        System.out.printf("d = %f\n",d);   //System.out.println("d = "+d);
    }
    
}
